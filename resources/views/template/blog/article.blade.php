			<p>
				Hupac a incontestablement profité de la reprise en 2021. Le groupe a, en effet, acheminé 1 123 562 UTI (Unités de Transport Intermodal), en hausse de 10,7 % par rapport à 2020. Ce trafic constitue un nouveau record, le précédent datant de 2019 avec 1 024 089 UTI. Surtout, cette augmentation représente l’équivalent de 100 000 camions en moins sur les routes.

			</p>
			<p>
				<h3 style="color: blue">Solution décarbonée</h3>
				Ce sont surtout les routes Transalpine via la Suisse et non-Transalpine qui ont porté cette croissance avec des hausses respectives de 11,8 % et 10,4 %. Pour la première, la hausse des trafics doit beaucoup à l’utilisation efficace du gabarit P400 (remorques de 4 mètres de hauteur) via le Gothard. Pour la seconde, l’augmentation provient du développement dynamique des trafics situés au Sud-Est et au Sud-Ouest de l’Europe.</p>
			<p>
				L’opérateur ERS Railways, acquis en juin 2018, a apporté sa contribution à ces résultats après une année 2020 difficile du fait de l’impact de la crise sanitaire. Selon Hupac, "le développement positif du trafic en 2021 n’est pas dû uniquement à la reprise économique mais également à l’intérêt croissant des logisticiens pour cette solution décarbonée et compétitive que représente le rail".

			</p>
			<p>
				Ce à quoi le patron du groupe, Michail Stahlhut, a ajouté : "Avec des services fiables, nous pouvons répondre aux attentes de l’industrie et reporter sur le rail de nouveaux volumes de trafic significatifs. Cela requiert, toutefois, un meilleur management des travaux réalisés sur le réseau, en particulier sur l’axe Rhin – Alpes. Les perturbations et les inefficacités tels que celles que nous avons subis durant le second semestre 2021 ne doivent pas se reproduire." L’amélioration d’itinéraires alternatifs comme celui reliant Karlsruhe à Bâle via la rive du Rhin sera donc cruciale à l’avenir.			</p>

			
			<p>
				<h3 style="color: blue">Explosion des coûts de l’énergie</h3>
				En attendant, le combiné rail-route est confronté à l’explosion des coûts de l’énergie. Selon les pays européens, cette augmentation du prix de l’électricité utilisée par les locomotives pour remorquer les convois a doublé, voire même triplée. "Nous pouvons uniquement remplir les objectifs du Green Deal qu’avec des prix compétitifs. Une subvention pour tout ce qui concourt à la traction des trains atténuerait les conséquences de la situation actuelle et enverrait un signal fort au marché", demande Michail Stahlhut.</p>
							
				<p>
					Hupac a, enfin, profité de l’exercice 2021 pour révéler sa stratégie pour les cinq années à venir. Avec une croissance de trafic de l’ordre de 7 % par an, le groupe devrait porter son trafic à 1,5 million d’UTI d’ici à 2026. Des développements clés dans des territoires comme l’Italie du Sud, l’Espagne et l’Est et le Sud-Est de l’Europe devraient contribuer à l’atteinte de ces objectifs qui passeront également par des investissements. Leur montant sera de 300 millions de francs suisses (295 millions d'euros) au cours de la période 2021-2026.
